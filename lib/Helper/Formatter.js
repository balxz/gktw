export const bold = (str) => {
    return `*${str}*`;
}

export const italic = (str) => {
    return `_${str}_`;
}

export const strikethrough = (str) => {
    return `~${str}~`;
}

export const quote = (str) => {
    return `> ${str}`;
}

export const monospace = (str) => {
    return `\`\`\`${str}\`\`\``
}

export const inlineCode = (str) => {
    return `\`${str}\``;
}